#!/bin/bash
# Скрипт для перезапуска сервера Xipher

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "=========================================="
echo "  Xipher - Перезапуск сервера"
echo "=========================================="
echo ""

# Остановка
echo "[1/2] Остановка сервера..."
"$SCRIPT_DIR/stop.sh"
sleep 2

# Запуск
echo ""
echo "[2/2] Запуск сервера..."
"$SCRIPT_DIR/start.sh"

echo ""
echo "=========================================="
echo "  Перезапуск завершен"
echo "=========================================="

